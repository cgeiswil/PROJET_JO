        <br>
​
        <strong>L'Olympisme : de belles valeurs qui rassemblent.</strong> Sachant certaines bellig&eacute;rances et les diff&eacute;rentes crises actuelles, 
        le fait que les athl&egrave;tes de chaques pays puissent concourir ensemble en oubliant le reste est sublime. En fait, le Comit&eacute; Internationnal Olympique souhaite que l'Excellence,
